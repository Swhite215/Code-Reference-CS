# Example Tar Commands, a tool for creating archive files called tarballs

tar # flags
# c, creates a tar archive
# x, extracts file from the archive
# t, displays table of contents
# v, verbose
# z, use compression
# f, file

tar -cf file/folder # create a tar archive
tar -zcf file/folder # create a compressed tar archive
tar -xf file.tar/folder.tar # extract files
